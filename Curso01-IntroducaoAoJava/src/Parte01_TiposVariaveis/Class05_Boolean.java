package Parte01_TiposVariaveis;

public class Class05_Boolean {
	
	public static void main(String[] args) {
		
		// Assume True ou False
		
		boolean valorA = true;
		boolean valorB = false;
		
		boolean NaoA = (!valorA) || valorB; // (Negação de A) || (B)
		
		System.out.println(NaoA);
		
		
		
	}

}
